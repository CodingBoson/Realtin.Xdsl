namespace Realtin.Xdsl.Xql
{
	/// <summary>
	/// Specifies a XQL operator, '== or !='.
	/// </summary>
	public enum XqlOperator : byte
	{
		/// <summary>
		/// The equals operator '=='.
		/// </summary>
		Equals,

		/// <summary>
		/// The not equals operator '!='.
		/// </summary>
		NotEquals,
	}
}